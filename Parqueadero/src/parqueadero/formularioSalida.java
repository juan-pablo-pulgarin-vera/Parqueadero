    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
       placa =txtPlaca.getText();
       fecha =dtFecha.getDate();
       hora = ftHora.getText();
       
       if(txtPlaca.getText().equals("") ||dtFecha.getDate() == null || ftHora.getText().equals("  :  ")){
           JOptionPane.showMessageDialog(rootPane, "Ningun campo debe quedar vacio");
       }else{
            formularioPrincipal.recibirInformacionSalida(txtPlaca.getText(),formatoFecha.format(fecha),ftHora.getText());
            txtPlaca.setText("");
            dtFecha.setDate(null);
            ftHora.setText("");
       }
       
    }//GEN-LAST:event_btnRegistrarMouseClicked

